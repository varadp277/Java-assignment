package MathOperations;

public class MathFunction {

    public static void Floor(double num) {
        System.out.println("Floor : " + Math.floor(num));
    }

    public static void Ceil(double num) {
        System.out.println("Ceil:"  + Math.ceil(num));
    }

    public static void Round(double num) {
        System.out.println("Round of :"  + Math.round(num));
    }
}
